uint32_t key_tick = 0;

void HAL_GPIO_EXTI_Callback(uint16_t Pin)
{
	if( ( HAL_GetTick()-key_tick ) <200 )
	{
		//如果时间间隔小于200ms,判定为信号抖动,检测作废
		return;
	}
	//在中断服务函数中点亮LED灯
	led_switch(LED_R,LED_ON);
	
	key_tick = HAL_GetTick();//获取当前时刻
}

