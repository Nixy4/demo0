//播放动画的函数
void play_animation(
	const uint8_t* anim[], uint16_t cnt, uint16_t delay, 
  uint16_t x,uint16_t y,uint16_t w, uint16_t h )
{
  for(uint16_t i=0;i<cnt;i++)
  {
    u8g2_ClearBuffer(&u8g2);
		
    u8g2_DrawXBM(&u8g2,x,y,w,h,anim[i]);
		
    u8g2_SendBuffer(&u8g2);
		
    HAL_Delay(delay);
  }
}
